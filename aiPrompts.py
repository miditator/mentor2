def generate_pure_vocabulary_task_prompt_ver2(lang_name, target_word, difficulty, history=None):
    """
    Генерирует предложение строго вокруг ОДНОГО целевого слова с учетом истории и уровня сложности.
    """
    diff = str(difficulty).strip().upper().replace("А", "A")
    level_rules = DIFFICULTY_RULES.get(diff, DIFFICULTY_RULES["A1"])

    history_condition = ""
    if history:
        history_lines = "\n".join([f"- {phrase}" for phrase in history])
        history_condition = f"""
🚨 ЗАПРЕТ НА ПОВТОРЫ: Следующие предложения на РУССКОМ языке уже были выданы пользователю сегодня. Тебе КАТЕГОРИЧЕСКИ ЗАПРЕЩЕНО генерировать их или их близкие синонимы:
{history_lines}
"""

    return f"""Ты — профессиональный ИИ-ментор и лингвист. Твоя задача — придумать ОДНО предложение на РУССКОМ языке, которое при переводе на {lang_name} язык будет строго соответствовать уровню сложности '{diff}'.

🚨 ГЛАВНОЕ ПРАВИЛО: Ты ОБЯЗАН построить это предложение вокруг ОДНОГО целевого слова (можешь менять форму/падеж): 
👉 {target_word} 👈
Категорически запрещено придумывать или использовать другие дополнительные слова из словаря пользователя! Предложение должно быть осмысленным и используемым в реальности.
{history_condition}
{level_rules}

Ответ напиши строго в 2 строки:
1 строка: только русское предложение (без кавычек).
2 строка: коротко (3-4 слов) укажи конкретное грамматическое правило, которое тестируется, и обязательно ДОБАВЬ в скобках ключевые слова на {lang_name} для этого правила. 
🚨 ВНИМАНИЕ: Напиши 2 строку СТРОГО на целевом ({lang_name}) языке. НИКОГДА не используй русский язык во второй строке."""


def answer_prompt(original_ru, user_answer, user_config):
    """Промпт для проверки перевода. Теперь ИИ проверяет ответ, основываясь на тех же жестких правилах!"""
    lang_name = "английский" if user_config.get("source_lang") == "en" else "немецкий"
    diff = str(user_config.get("difficulty", "A1")).strip().upper().replace("А", "A")
    level_rules = DIFFICULTY_RULES.get(diff, DIFFICULTY_RULES["A1"])

    return (
        f"Ты — строгий, но поддерживающий ИИ-Ментор по языку. Твоя задача — проверить перевод пользователя.\n"
        f"Оригинальная фраза на русском: '{original_ru}'\n"
        f"Пользователь перевел на {lang_name} язык: '{user_answer}'\n"
        f"Целевой уровень сложности: {diff}\n\n"
        f"Учти критерии этого уровня при проверке:\n{level_rules}\n\n"
        f"Проанализируй ответ. Если перевод верный и грамматически точный для уровня {diff}, начни свой ответ строго с префикса '✅'.\n"
        f"Если в переводе есть критические ошибки, опечатки или он не соответствует правилу, начни ответ строго с префикса '❌'.\n\n"
        f"Дай короткий, понятный разбор на русском языке: укажи на ошибки (если они есть), предложи идеальный вариант и похвали за правильные моменты. Напиши лаконично, без лишней воды."
    )

